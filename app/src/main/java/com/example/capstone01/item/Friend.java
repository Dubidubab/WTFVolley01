package com.example.capstone01.item;

public class Friend {

    public String title;
    public String email;
    public String name;

    public Friend() {
        this.title = title;
        this.email = email;
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setName(String name) {
        this.name = name;
    }
}
